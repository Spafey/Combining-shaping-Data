from math import *

my_num = -5

#gives absolute value
print(abs(my_num))

#applies the power function
print(pow(4, 6))

#prints highest/lowest number from list
print(min(4, 6, 9))
print(max(4, 6, 9))

#round function
print(round(3.2))

print("~~~~~~~~~~~~~~~~~\n\n")

#math from library math

#floor always grabs lowest whole number
print(floor(3.7))

#ceil always rounds up
print(ceil(3.7))

#gives square root
print(sqrt(25))